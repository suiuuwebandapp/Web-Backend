    $(document).ready(function(){
        $("#menu_div a").bind("click",function(){
            var x=$("#menu_div").height();
            var top=$($(this).attr("href")).offset().top;
            if($("#menu_div").css('position')!='fixed'){
                $("html, body").animate({scrollTop: top-x-x-10 + "px"}, {duration: 500,easing: "swing"});
            }else{
                $("html, body").animate({scrollTop: top-x-10 + "px"}, {duration: 500,easing: "swing"});
            }
            return false;
        });
    });
    $(function(){

        $(window).scroll(function(e) {
            var h1=$(window).scrollTop();
            var maxHeight=$('.product_banner').height();
            if(h1>maxHeight){
                $('.rightFixed').addClass('active')
                $('.topTip').addClass('active')
            }else{
                $('.rightFixed').removeClass('active')
                $('.topTip').removeClass('active')
            }

        });

        $('.rightFixed .codeBox .goTop').click(function(e) {
            $('html,body').scrollTop(0);

        });


        $('.showBtn').click(function(e) {
            $('.rightFixed').toggle();
        });


    })
